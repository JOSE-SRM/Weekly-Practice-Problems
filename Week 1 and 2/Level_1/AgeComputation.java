// Program to find the Age of Harry who is born in 2000 and the Current year is 2024

class AgeComputation{
  public static void main(String[] args) {
    
    int dateOfBirthOfHarry = 2000;
    int currentYear = 2024;

    int ageOfHarry = currentYear - dateOfBirthOfHarry;

    System.out.println("Age of Harry: " + ageOfHarry);
  }
}
